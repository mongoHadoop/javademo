    /**
     * Acquires in exclusive mode, ignoring interrupts.  Implemented
     * by invoking at least once {@link #tryAcquire},
     * returning on success.  Otherwise the thread is queued, possibly
     * repeatedly blocking and unblocking, invoking {@link
     * #tryAcquire} until success.  This method can be used
     * to implement method {@link Lock#lock}.
     *
     * @param arg the acquire argument.  This value is conveyed to
     *        {@link #tryAcquire} but is otherwise uninterpreted and
     *        can represent anything you like.
     *
     * acquire 对整个 AQS 做到了承上启下的作用，通过 tryAcquire 模版方法进行尝试获取锁，获取锁失败包装当前线程为 Node 节点加入等待队列排队
     *
     * tryAcquire 是 AQS 中抽象模版方法，但是内部会有默认实现，虽然默认的方法内部抛出异常，为什么不直接定义为抽象方法呢?
     * 因为 AQS 不只是对独占锁实现了抽象，同时还包括共享锁；不同锁定义了不同类别的方法，共享锁就不需要 tryAcquire，
     *            如果定义为抽象方法，继承 AQS 子类都需要实现该方法
     *
     */
    public final void acquire(int arg) {

        //1 tryAcquire 再次获取锁
        //2 由于 tryAcquire 做了取反，如果设置 state 失败并且独占锁线程不是自己本身返回 false，通过取反会进入接下来的流程
        if (!tryAcquire(arg) &&
              //2.1  Node 入队流程 addWaiter 方法就是为了让 Node 入队，并且维护出一个双向队列模型
                //入队执行成功后，会在 acquireQueued 再次尝试竞争锁，竞争失败后会将线程阻塞
            acquireQueued(addWaiter(Node.EXCLUSIVE), arg))
            // 3 如果线程被中断, 这里会再次设置中断状态
            // 3.1 因为如果线程中断, 调用 Thread.interrupted 虽然会返回 true, 但是会清除线程中断状态
            selfInterrupt();
    }

